import React, { useState } from 'react'
import '../styles/StyleLogIn.css'
import clockPng from '../assets/clock.png'

export default function LogIn({ onLogin }) {
	const [loginData, setLoginData] = useState({ email: '', password: '', remember: false })
	const [regData, setRegData] = useState({
		nombreCompleto: '',
		cedula: '',
		telefono: '',
		fechaNacimiento: '',
		genero: '',
		rol: '',
		email: '',
		password: '',
		confirmarPassword: '',
	})
	const [regSuccess, setRegSuccess] = useState(false)
	const [loginError, setLoginError] = useState('')

	const handleLoginChange = (e) => {
		const { name, value, type, checked } = e.target
		setLoginData((p) => ({ ...p, [name]: type === 'checkbox' ? checked : value }))
	}

	const handleLoginSubmit = (e) => {
		e.preventDefault()
		setLoginError('')
		// validate against users saved in localStorage
		const raw = localStorage.getItem('crono_users')
		const users = raw ? JSON.parse(raw) : []
		const found = users.find((u) => u.email === loginData.email && u.password === loginData.password)
		if (found) {
			// persist current user
			localStorage.setItem('crono_current_user', JSON.stringify(found))
			if (onLogin) onLogin(found)
		} else {
			setLoginError('Usuario o contraseña incorrectos')
		}
	}

	const handleRegChange = (e) => {
		const { name, value } = e.target
		setRegData((p) => ({ ...p, [name]: value }))
	}

	const handleRegSubmit = (e) => {
		e.preventDefault()
		// basic validation: passwords match
		if (regData.password !== regData.confirmarPassword) {
			alert('Las contraseñas no coinciden')
			return
		}
		const raw = localStorage.getItem('crono_users')
		const users = raw ? JSON.parse(raw) : []
		// avoid duplicate emails
		if (users.find((u) => u.email === regData.email)) {
			alert('El correo ya está registrado')
			return
		}
		const saveUser = {
			nombreCompleto: regData.nombreCompleto,
			cedula: regData.cedula,
			telefono: regData.telefono,
			fechaNacimiento: regData.fechaNacimiento,
			genero: regData.genero,
			rol: regData.rol,
			email: regData.email,
			password: regData.password,
		}
		users.push(saveUser)
		localStorage.setItem('crono_users', JSON.stringify(users))
		setRegSuccess(true)
		setTimeout(() => setRegSuccess(false), 3000)
	}

	return (
		<div className="main_log_in">
			<div className="container_title_crono">
				<h1 className="featurette-heading fw-normal lh-1 titleCrono">
					Cron
					<span>
						<img className="userCrono" src={clockPng} alt="clock" />
					</span>
					Clase
				</h1>
			</div>

			<div className="container_log_in">
				<form id="formLogin" className="cardSinIng" onSubmit={handleLoginSubmit}>
					<h1 className="h3 mb-3 fw-normal log_in_title">Inicio de sesion</h1>
					<div className="form-floating mb-2">
						<input
							type="email"
							className="form-control"
							id="floatingInput"
							placeholder="name@example.com"
							name="email"
							value={loginData.email}
							onChange={handleLoginChange}
							required
						/>
						<label htmlFor="floatingInput">Email</label>
					</div>

					<div className="form-floating mb-2">
						<input
							type="password"
							className="form-control"
							id="floatingPassword"
							placeholder="Password"
							name="password"
							value={loginData.password}
							onChange={handleLoginChange}
							required
						/>
						<label htmlFor="floatingPassword">Contraseña</label>
					</div>

					{loginError && <div className="text-danger mb-2">{loginError}</div>}

					<div className="form-check text-start my-3">
						<input
							className="form-check-input"
							type="checkbox"
							id="checkDefault"
							name="remember"
							checked={loginData.remember}
							onChange={handleLoginChange}
						/>
						<label className="form-check-label" htmlFor="checkDefault"></label>
						Guardar datos
					</div>

					<div className="d-flex gap-2">
						<button className="btn btn-primary w-100 py-2 buttCrono" type="submit">
							Entrar
						</button>
						<button
							className="btn btn-outline-primary w-100 py-2 buttCrono"
							type="button"
							data-bs-toggle="modal"
							data-bs-target="#modalRegistro"
						>
							Registrarse
						</button>
					</div>
				</form>

				<div className="modal fade" id="modalRegistro" tabIndex="-1" aria-labelledby="modalRegistroLabel" aria-hidden="true">
					<div className="modal-dialog">
						<div className="modal-content">
							<div className="modal-header">
								<h5 className="modal-title" id="modalRegistroLabel">Registro de usuario</h5>
								<button type="button" className="btn-close" data-bs-dismiss="modal" aria-label="Close"></button>
							</div>
							<div className="modal-body">
								<form id="formRegistro" onSubmit={handleRegSubmit}>
									<div className="mb-3">
										<label htmlFor="nombreCompleto" className="form-label">Nombre completo</label>
										<input type="text" className="form-control" id="nombreCompleto" name="nombreCompleto" value={regData.nombreCompleto} onChange={handleRegChange} required />
									</div>

									<div className="mb-3">
										<label htmlFor="cedula" className="form-label">Cédula</label>
										<input type="text" className="form-control" id="cedula" name="cedula" value={regData.cedula} onChange={handleRegChange} required />
									</div>

									<div className="mb-3">
										<label htmlFor="telefono" className="form-label">Teléfono</label>
										<input type="tel" className="form-control" id="telefono" name="telefono" value={regData.telefono} onChange={handleRegChange} required />
									</div>

									<div className="mb-3">
										<label htmlFor="fechaNacimiento" className="form-label">Fecha de nacimiento</label>
										<input type="date" className="form-control" id="fechaNacimiento" name="fechaNacimiento" value={regData.fechaNacimiento} onChange={handleRegChange} required />
									</div>

									<div className="mb-3">
										<label htmlFor="genero" className="form-label">Género</label>
										<select className="form-select" id="genero" name="genero" value={regData.genero} onChange={handleRegChange} required>
											<option value="">Selecciona una opción</option>
											<option value="masculino">Masculino</option>
											<option value="femenino">Femenino</option>
											<option value="otro">Otro</option>
										</select>
									</div>

									<div className="mb-3">
										<label className="form-label">Rol</label>
										<select className="form-select" id="rol" name="rol" value={regData.rol} onChange={handleRegChange} required>
											<option value="">Selecciona una opción</option>
											<option value="docente">Docente</option>
											<option value="estudiante">Estudiante</option>
										</select>
									</div>

									<div className="mb-3">
										<label htmlFor="emailRegistro" className="form-label">Correo electrónico</label>
										<input type="email" className="form-control" id="emailRegistro" name="email" value={regData.email} onChange={handleRegChange} required />
									</div>

									<div className="mb-3">
										<label htmlFor="passwordRegistro" className="form-label">Contraseña</label>
										<input type="password" className="form-control" id="passwordRegistro" name="password" value={regData.password} onChange={handleRegChange} required />
									</div>

									<div className="mb-3">
										<label htmlFor="confirmarPassword" className="form-label">Confirmar contraseña</label>
										<input type="password" className="form-control" id="confirmarPassword" name="confirmarPassword" value={regData.confirmarPassword} onChange={handleRegChange} required />
									</div>

									<button type="submit" className="btn btn-primary w-100 py-2 buttCrono">Registrarse</button>

									{regSuccess && <div id="mensajeRegistro" className="mt-3 text-success">Te has registrado exitosamente</div>}
								</form>
							</div>
						</div>
					</div>
				</div>
			</div>
		</div>
	)
}